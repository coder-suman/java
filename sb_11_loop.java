package com.company;

public class sb_11_loop {
    public static void main(String[] args) {

//        while loop
        int a = 0;
        while (a<=10){
            System.out.println(a);
            a++;
        }

//        Do while loop
        int i = 0;
        do{
            System.out.println(i);
            i++;
        }while(i<=10);

//           for loop
        for(int x=0;x<=10;x++){
            System.out.println(x);
        }

//        reverse for loop
        for (int y=10;y>=0;y--){
            System.out.println(y);
        }
    }


}
